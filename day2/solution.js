// with for
let sum = 0;
for (let i = 1; i <= 100; i++) {
  sum += i;
}
console.log("Sum of numbers from 1 to 100 with for is:", sum);

// with while
let total = 0;
let index = 1;
while (index <= 100) {
  total += index;
  index++;
}
console.log("Sum of numbers from 1 to 100 with while is:", total);

